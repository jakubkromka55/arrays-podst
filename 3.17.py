# Tworzymy krotkę z liczbami
my_tuple = (50, 20, 40, 50, 30, 50)

# Wartość, której szukamy
value = 50

# Zliczamy liczbę wystąpień wartości w krotce
occurrences = my_tuple.count(value)

# Wypisujemy wyniki
print(f"Tuple: {my_tuple}")
print(f"Value: {value}")
print(f"Number of occurrences: {occurrences}")
